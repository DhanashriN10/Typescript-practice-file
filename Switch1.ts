let p = 3;  
let q = 2;  
  
switch (p+q)
{  
    case 1: 
    {  
        console.log("p+q is 1.");  
        break;  
    }  
    case 2: 
    {  
        console.log("p+q is 5.");  
        break;  
    }  
    case 3: 
    {  
        console.log("p+q is 6.");  
        break;  
    }  
      
    default: 
    {  
        console.log("p+q is 5.");  
        break;  
    }  
}  