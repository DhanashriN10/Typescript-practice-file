var p = 3;
var q = 2;
switch (p + q) {
    case 1:
        {
            console.log("p+q is 1.");
            break;
        }
    case 2:
        {
            console.log("p+q is 5.");
            break;
        }
    case 3:
        {
            console.log("p+q is 6.");
            break;
        }
    default:
        {
            console.log("p+q is 5.");
            break;
        }
}
